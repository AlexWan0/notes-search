def split_on_indents(raw, indent_char='\n'):
    result = []
    
    curr_str = []
    curr_num_indent = 0
    prev_is_indent = False
    for c in raw:
        if not prev_is_indent and c == indent_char:
            result.append((curr_num_indent, ''.join(curr_str)))
        
            curr_str = []
            curr_num_indent = 0
        
        if c == indent_char:
            curr_num_indent += 1
        else:
            curr_str.append(c)
        
        prev_is_indent = (c == indent_char)
    
    if len(curr_str) > 0:
        result.append((curr_num_indent, ''.join(curr_str)))
    
    return result


def make_line_map(root_node: Line) -> dict[int, Line]:
    return {
        node.line_idx: node for node in root_node.children
    }


